#include "ft_printf.h"

int	ft_p(unsigned long p)
{
	int		i;
	char	*hex;

	i = 0;
	hex = "0123456789abcdef";
	if (p >= 16)
	{
		i += ft_p(p / 16);
		i += ft_p(p % 16);
	}
	else
		i += ft_putchar(hex[p % 16]);
	return (i);
}
