#include "ft_printf.h"

int	ft_x(unsigned int a)
{
	int		i;
	char	*hex;

	i = 0;
	hex = "0123456789abcdef";
	if (a >= 16)
	{
		i += ft_x(a / 16);
		i += ft_x(a % 16);
	}
	else
	{
		i += ft_putchar(hex[a % 16]);
	}
	return (i);
}
