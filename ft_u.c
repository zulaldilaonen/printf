#include "ft_printf.h"

int	ft_u(unsigned int a)
{
	int	i;

	i = 0;
	if (a <= 9)
		i += ft_putchar(a + 48);
	else
	{
		i += ft_u(a / 10);
		i += ft_u(a % 10);
	}
	return (i);
}
